    public void keyReleased(KeyEvent e) {
        comparePasswords();
    }

    private void comparePasswords() {
        String p1 = new String(txtNewPassword.getPassword());
        String p2 = new String(txtConfirmPassword.getPassword());
        if (p1.equals(p2)) {
            if (p1.length() >= 6 ) {
                btnChange.setEnabled(true);
            }
        } else {
            btnChange.setEnabled(false);
        }
    }
    
    private boolean passwordMatched() {
        Connection conn = DbConnection.getConnection();
        try
        {
            PreparedStatement stat = conn.prepareStatement("select count(*) from users where username=? and password=?");
            stat.setString(1, (String)cmbUser.getSelectedItem());
            stat.setString(2, new String(txtOldPassword.getPassword()));
            ResultSet rs = stat.executeQuery();
            rs.next();
            int c = rs.getInt(1);
            if (c ==1)
            {
                return true;
            }
        }catch(Exception e){}
        return false;
    }

    private void changePassword() {
        Connection conn = DbConnection.getConnection();
        try
        {
            PreparedStatement stat = conn.prepareStatement("update users set password=? where username=?");
            stat.setString(2, (String)cmbUser.getSelectedItem());
            stat.setString(1, new String(txtConfirmPassword.getPassword()));
            int c = stat.executeUpdate();
            if (c >= 1)
            {
                JOptionPane.showMessageDialog(null, "Password Successfully changed");
                MyLogger.writeLog("Password changed", " new password ");
                txtOldPassword.setText("");
                txtNewPassword.setText("");
                txtConfirmPassword.setText("");
            }
        }catch(Exception e){}
    }
