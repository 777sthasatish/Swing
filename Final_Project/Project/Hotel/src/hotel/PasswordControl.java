    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() == btnChange) {
            if (cmbUser.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(null, "please select user first ");
                return;
            }
            if (txtOldPassword.getPassword().length == 0) {
                JOptionPane.showMessageDialog(null, "please enter old password first");
                return;
            }
            if (passwordMatched())
            {
                changePassword();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Old Password doesnot match");
            }
        } 
    }
    
